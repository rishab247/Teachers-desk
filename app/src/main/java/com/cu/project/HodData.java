package com.cu.project;

public class HodData {

    String name;
    String eid;

    public HodData(String name, String eid) {
        this.name = name;
        this.eid = eid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEid() {
        return eid;
    }

    public void setEid(String eid) {
        this.eid = eid;
    }
}
